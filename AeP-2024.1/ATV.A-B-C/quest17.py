#17. Leia o valor da base e altura de um retângulo, calcule e escreva sua área. (área = base * altura)

#Entrada
print("Vamos calcular a área de um  retângulo!")
print(".......................................")
base = float(input("> Informe o valor da base do retângulo, por favor: "))
altura = float(input("> Informe a altura do retângulo: "))

#Processamento
area = base * altura

#Saída
print(f"> Com base nos dados fornecidos, a área desse retângulo é igual a: {area}.")