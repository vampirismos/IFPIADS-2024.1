#12. Leia o salário de um trabalhador e escreva seu novo salário com um aumento de 25%.

#Entrada
print("Hora de calcular o aumento de 25%. em seu salário!")
salario = float(input("> Por favor, digite seu salário atual: "))

#Processamento
aumento = salario + (salario * 0.25)

#Saída
print(f"> O valor total de seu salário após o aumento de 25% será igual a: {aumento}.")

